
public class uTrougao {
 double Z1,Z2,Z3;
	
	
	public static double Z12(double Z1,double Z2,double Z3) {
		
		return ((Z1*Z2)+(Z2*Z3)+(Z1*Z3))/Z3;
	}
	public static double Z13(double Z1,double Z2,double Z3) {
		
		return ((Z1*Z2)+(Z2*Z3)+(Z1*Z3))/Z2;
		
	}
	
	public static double  Z23(double Z1,double Z2,double Z3) {
		
		
		return ((Z1*Z2)+(Z2*Z3)+(Z1*Z3))/Z1;
		
	}
	
	
}
